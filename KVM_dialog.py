    def azimut_a_dxdy(self,az_deg, longitud):
        rad = np.deg2rad(az_deg)
        dx = longitud * np.sin(rad)
        dy = longitud * np.cos(rad)
        return dx, dy

    def graphic_az(self,az_before, az_anomalo, az_after, long, output):
        # initial point
        x0, y0 = 0, 0

        # Vector 1: az_before
        dx1, dy1 = self.azimut_a_dxdy(az_before, long)
        x1, y1 = x0 + dx1, y0 + dy1

        # Vector 2: az_anomalo
        dx2, dy2 = self.azimut_a_dxdy(az_anomalo, long)
        x2, y2 = x1 + dx2, y1 + dy2

