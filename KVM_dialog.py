    def date_to_julian(self,yyyymmdd,item):
        if item=='YYYY.YYYYYYYY': 
            # Year
            year = int(float(yyyymmdd))
            # Initial day of the year
            start_of_year = datetime(year, 1, 1)
            # Inital day of the next year
            start_of_next_year = datetime(year + 1, 1, 1)
            # Duration of the year in days (365 o 366)
            year_length = (start_of_next_year - start_of_year).days
            # Fractio of year (In days)
            days_passed = (float(yyyymmdd) - year) * year_length
            # Resulting date
            target_date = start_of_year + timedelta(days=days_passed)
            # Convert the integer or string YYYYMMDD to a string for parsing
            date_str = str(target_date.strftime("%Y%m%d"))
        else:
            # Convert the integer or string YYYYMMDD to a string for parsing
            date_str = str(yyyymmdd)

        # Extract year, month, day as integers
        Y = int(date_str[0:4])
        M = int(date_str[4:6])
        D = int(date_str[6:8])
        
        # Adjustment for the Gregorian calendar according to the formula for the astronomical Julian date
        # If the month is January or February, it is considered as the 13th and 14th month of the previous year.
        if M <= 2:
            Y -= 1
            M += 12
        
        A = Y // 100
        B = 2 - A + (A // 4)
        
        # Calculation of the astronomical Julian date
        JD = int(365.25*(Y + 4716)) + int(30.6001*(M + 1)) + D + B - 1524.5
        
        return JD

    def julian_to_ddmmyyyy(self,jd):
        """
        Convert an astronomical Julian day to the DD-MM-YYYY format (string).
        Args:
            jd (float): astronomical Julian date
        Returns:
            str: Date DD-MM-YYYY format
        """
        jd = jd + 1
        Z = math.floor(jd)
        F = jd - Z
        
        alpha = math.floor((Z - 1867216.25) / 36524.25)
        
        if Z > 2299160:
            A = Z + 1 + alpha - math.floor(alpha / 4)
        else:
            A = Z
        
        B = A + 1524
        C = math.floor((B - 122.1) / 365.25)
        D = math.floor(365.25 * C)
        E = math.floor((B - D) / 30.6001)
        
        day = B - D - math.floor(30.6001 * E) + F
        
        if E < 13.5:
            month = E - 1
        else:
            month = E - 13
        
        if month > 2.5:
            year = C - 4716
        else:
            year = C - 4715
        
        # To get the date of the calendar day (00:00-23:59), use floor in day
        day_int = math.floor(day)
        
        # DD-MM-YYYY format
        dd = f"{day_int:02d}"
        mm = f"{int(month):02d}"
        yyyy = f"{int(year):04d}"
        
        return f"{dd}-{mm}-{yyyy}"

