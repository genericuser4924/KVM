                        # Inital day of the next year
                        start_of_next_year = datetime(year + 1, 1, 1)
                        # Duration of the year in days (365 o 366)
                        year_length = (start_of_next_year - start_of_year).days
                        # Fractio of year (In days)
                        days_passed = (float(df.iloc[0][met_sl]) - year) * year_length
