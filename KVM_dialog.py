    # Function for create vector layer with data lat and long of the file
    def up_canvas(self):
        op_lat = self.lat.currentText()
        op_long = self.longi.currentText()

        # Define type of geometry (point) and CRS
        geom_type = QgsWkbTypes.Point
        crs = "EPSG:4326"  # CRS WGS 84

        # Create the temporal layer
        uri = "Point?crs=" + crs
        layer = QgsVectorLayer(uri, "Position", "memory")

        # Define fields of the attrib's tables 
        if Qgis.versionInt() >= 33800:
            fields = [
                QgsField("Date", QMetaType.Type.QString),
                QgsField("Lat", QMetaType.Type.Double),
                QgsField("Long", QMetaType.Type.Double),
            ]
        else:
            fields = [
                QgsField("Date", QVariant.String),
                QgsField("Lat", QVariant.Double),
                QgsField("Long", QVariant.Double),
            ]

        layer.dataProvider().addAttributes(fields)
        layer.updateFields()

        # Enable edit
        layer.startEditing()

        # Add point of list
        for i in range(len(df)):
            feature1 = QgsFeature()
            pos_1=list_col[0]
            C_F=str(df.iloc[i][pos_1])
            C_N=float(df.iloc[i][op_lat])
            C_E=float(df.iloc[i][op_long])
            if C_E>180:
                C_E=C_E-360
            feature1.setGeometry(QgsGeometry.fromPointXY(QgsPointXY(C_E,C_N)))
            feature1.setAttributes([C_F,C_N,C_E])
            layer.addFeature(feature1)

        # Save the change
        layer.commitChanges()

        # Generates point symbols in vectors.
        ruta_estilo = os.path.join(os.path.dirname(__file__), 'scripts', 'point.qml') 
        processing.run("native:setlayerstyle", {'INPUT':layer,'STYLE':ruta_estilo})

        # Add layer to the project
        QgsProject.instance().addMapLayer(layer)

        # Obtain the geographic extent of the layer
        extent = layer.extent()

        # Access the map canvas and apply zoom
        canvas = iface.mapCanvas()
        canvas.setExtent(extent)
        canvas.refresh()


        # Add mapbase is required
        if self.mapbase.isChecked():
            op_url = self.tiles.currentText()
            url = op_tiles[op_url]
            rasterLayer = QgsRasterLayer("type=xyz&url=" + url, str(op_url), "wms")
            QgsProject.instance().addMapLayer(rasterLayer, False)
            root = QgsProject.instance().layerTreeRoot()
            root.insertLayer(-1, rasterLayer)

        self.Boton1.setEnabled(True)
        self.adv_1.setText("<font style='color:#32CD32'><b>The CORS has been loaded successfully.</b></font>")


