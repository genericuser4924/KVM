    # Function to calculate the circular standard deviation
    def circular_dec_deg(self, theta_deg):
        theta_rad = np.radians(theta_deg)
        # Cos and Sen
        c_s = np.sum(np.cos(theta_rad))
        s_s = np.sum(np.sin(theta_rad))
        mod_med = (((c_s**2)+(s_s**2))**0.5)/(len(theta_rad))
        var_cir = 1 - mod_med
        std_angle = (180/math.pi)*((-2*(math.log(1-var_cir))))**0.5 # circular standard deviation (deg)  
        return std_angle

    # Function to calculate the circular mean (radian)   
    def circular_mean_deg(self, theta_deg):
        theta = np.deg2rad(theta_deg)
        c = np.mean(np.cos(theta))
        s = np.mean(np.sin(theta))
        mu = np.arctan2(s, c)
        return np.rad2deg(mu) % 360

    # Function to calculate the angular distance between azimuths (deg)
    def angular_distance_deg(self, a, b):
        d = np.abs(a - b) % 360
        return np.minimum(d, 360 - d)

    """
    Watson–Wheeler / Mardia–Watson–Wheeler test using circular ranks,
    as in the literature scheme with di = 360 * rank / nc.
    """       
    def mardia_watson_wheeler_stat(self, theta1_deg, theta2_deg):
        # 1) Sample sizes
        n1 = len(theta1_deg)
        n2 = len(theta2_deg)
        nc = n1 + n2
        
        # 2) Pool data and compute (linear) ranks
        theta_all = np.r_[theta1_deg, theta2_deg]
        group = np.r_[np.zeros(n1, dtype=int), np.ones(n2, dtype=int)]
        
        ranks = np.empty(nc, dtype=float)
        order = np.argsort(theta_all)
        ranks[order] = np.arange(1, nc + 1)  # 1..nc
        
        # 3) Circular ranks (di) in degrees, as in the figure
        d = 360.0 * ranks / nc
        
        # 4) sin di and cos di
        d_rad = np.deg2rad(d)
        cos_d = np.cos(d_rad)
        sin_d = np.sin(d_rad)
        
        # 5) Sum within groups
        c1 = np.sum(cos_d[group == 0])
        s1 = np.sum(sin_d[group == 0])
        
        c2 = np.sum(cos_d[group == 1])
        s2 = np.sum(sin_d[group == 1])
        
        # 6) W statistic as shown in the table
        W = 2 * ((c1**2 + s1**2) / n1 + (c2**2 + s2**2) / n2)
        
        p_value_mwww = 1 - chi2.cdf(W, df=2)
        return W, p_value_mwww


    # Resultant vector length R for a sample in radians.
    def _resultant_length(self,theta_rad):
        C = np.sum(np.cos(theta_rad))
        S = np.sum(np.sin(theta_rad))
        R = np.sqrt(C**2 + S**2)
        return R


    """
    Watson–Williams test for equality of circular means
    between two groups of azimuths in degrees.
    """    
    def watson_williams(self, theta1_deg, theta2_deg, az_anomalo):
        n1 = len(theta1_deg)
        n2 = len(theta2_deg)
        N = n1 + n2
        k = 2

        if n1 < 2 or n2 < 2:
            note="Each group must contain at least two observations." 

        theta1_rad = np.deg2rad(theta1_deg)
        theta2_rad = np.deg2rad(theta2_deg)
        theta_all_rad = np.deg2rad(np.r_[theta1_deg, theta2_deg])

        # Circular means
        mu1 = self.circular_mean_deg(theta1_deg)
        mu2 = self.circular_mean_deg(theta2_deg)
        mu_global = self.circular_mean_deg(np.r_[theta1_deg, theta2_deg])

        # Resultant lengths
        R1 = self._resultant_length(theta1_rad)
        R2 = self._resultant_length(theta2_rad)
        R = self._resultant_length(theta_all_rad)

        # Mean resultant lengths
        r1 = R1 / n1
        r2 = R2 / n2
        r = R / N

        # Watson–Williams F statistic
        A = R1 + R2
        B = R

        numerator = (N - k) * (A - B)
        denominator = (k - 1) * (N - A)

        if denominator <= 0:
            F_stat = np.nan
            p_value_ww = np.nan
        else:
            F_stat = numerator / denominator
            p_value_ww = 1 - f.cdf(F_stat, dfn=k - 1, dfd=N - k)

        return F_stat, p_value_ww


