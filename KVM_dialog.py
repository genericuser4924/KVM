        ax.quiver(x1, y1, dx2, dy2, angles='xy', scale_units='xy', scale=1,
                  color='red', width=0.005, label='az_anomalo')
        ax.quiver(x2, y2, dx3, dy3, angles='xy', scale_units='xy', scale=1,
                  color='green', width=0.005, label='az_after')

        ax.plot([x0, x1, x2, x3], [y0, y1, y2, y3], 'ko', markersize=4)

        ax.text(x0 + dx1/2, y0 + dy1/2, 'az_before', color='blue')
        ax.text(x1 + dx2/2, y1 + dy2/2, 'az_anomaly', color='red')
