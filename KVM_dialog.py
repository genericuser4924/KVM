    # Convert decimal degree to GG.MM.SS,SSSSS 
    def dec_a_ggmmss(self,deg_dec):
        
        positivo = abs(deg_dec)
        
        # Extrac the components
        grados = int(positivo)
        minutos_restantes = (positivo - grados) * 60
        minutos = int(minutos_restantes)
        segundos = (minutos_restantes - minutos) * 60
        
        if deg_dec < 0:
            grados = -grados        
        
        return f"{grados:02d}°{minutos:02d}'{segundos:08.5f}\""

